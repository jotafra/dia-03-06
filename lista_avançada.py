# Definindo uma lista de números inteiros
numeros = [10, 5, 7, 3, 2]

# Exibindo a lista
print("Lista de números:", numeros)

# Removendo um número específico da lista
numeros.remove(7)
print("Lista após remover o número 7:", numeros)

# Verificando o comprimento da lista
print(f"Comprimento da lista: {len(numeros)}")